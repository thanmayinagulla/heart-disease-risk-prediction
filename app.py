# =========================
# TITLE
# =========================
st.title("❤️ Heart Disease AI System")

# =========================
# PDF FUNCTION
# =========================
def generate_pdf(name, age, prob, result):

    file_name = "heart_report.pdf"
    doc = SimpleDocTemplate(file_name)

    styles = getSampleStyleSheet()

    title_style = ParagraphStyle(
        "title",
        parent=styles["Title"],
        fontSize=20,
        textColor=colors.darkred,
        alignment=1
    )

    heading_style = ParagraphStyle(
        "heading",
        parent=styles["Heading2"],
        textColor=colors.darkblue
    )

    normal_style = ParagraphStyle(
        "normal",
        parent=styles["Normal"],
        fontSize=11,
        textColor=colors.black
    )

    risk_style = ParagraphStyle(
        "risk",
        parent=styles["Normal"],
        fontSize=12
    )

    content = []

    content.append(Paragraph("HEART DISEASE AI REPORT", title_style))
    content.append(Spacer(1, 10))

    content.append(Paragraph("PATIENT DETAILS", heading_style))
    content.append(Paragraph(f"Name: {name}", normal_style))
    content.append(Paragraph(f"Age: {age}", normal_style))
    content.append(Spacer(1, 10))

    content.append(Paragraph("PREDICTION RESULT", heading_style))
    content.append(Paragraph(f"Result: {result}", normal_style))
    content.append(Paragraph(f"Risk Probability: {prob:.2f}%", normal_style))
    content.append(Spacer(1, 10))

    if prob >= 70:
        risk_text = "HIGH RISK - Immediate attention required"
        risk_style.textColor = colors.red
    elif prob >= 40:
        risk_text = "MODERATE RISK - Lifestyle changes needed"
        risk_style.textColor = colors.orange
    else:
        risk_text = "LOW RISK - Healthy condition"
        risk_style.textColor = colors.green

    content.append(Paragraph("RISK LEVEL", heading_style))
    content.append(Paragraph(risk_text, risk_style))

    content.append(Spacer(1, 20))
    content.append(Paragraph("Generated by AI Heart Disease System", normal_style))

    doc.build(content)

    return file_name

# =========================
# INPUTS
# =========================
st.subheader("🧑 Patient Details")

name = st.text_input("Patient Name")

age = st.number_input("Age", 1, 120, 45)

sex = st.selectbox("Gender", ["Female (0)", "Male (1)"])

cp = st.selectbox("Chest Pain", ["0", "1", "2", "3"])

trestbps = st.number_input("Blood Pressure", 80, 250, 120)

chol = st.number_input("Cholesterol", 100, 600, 200)

fbs = st.selectbox("FBS", ["0", "1"])

restecg = st.selectbox("ECG", ["0", "1", "2"])

thalach = st.number_input("Max Heart Rate", 60, 250, 150)

exang = st.selectbox("Exercise Angina", ["0", "1"])

oldpeak = st.number_input("Oldpeak", 0.0, 10.0, 1.0)

slope = st.selectbox("Slope", ["0", "1", "2"])

ca = st.selectbox("CA", ["0", "1", "2", "3"])

thal = st.selectbox("Thal", ["0", "1", "2", "3"])

# =========================
# PREDICTION
# =========================
if st.button("🚀 Predict"):

    sex = 1 if sex == "Male (1)" else 0

    data = np.array([[
        age, sex, int(cp), trestbps, chol,
        int(fbs), int(restecg), thalach,
        int(exang), oldpeak, int(slope),
        int(ca), int(thal)
    ]])

    prob = model.predict_proba(data)[0][1]
    pred = model.predict(data)[0]

    st.markdown("---")

    st.write(f"### ❤️ Risk Probability: {prob*100:.2f}%")

    if prob >= 0.7:
        result = "HIGH RISK"
        st.error("🔴 HIGH RISK DETECTED")
    elif prob >= 0.4:
        result = "MODERATE RISK"
        st.warning("🟡 MODERATE RISK")
    else:
        result = "LOW RISK"
        st.success("🟢 LOW RISK")

    # =========================
    # PDF DOWNLOAD
    # =========================
    if name != "":
        pdf_file = generate_pdf(name, age, prob*100, result)

        with open(pdf_file, "rb") as f:
            st.download_button(
                "📄 Download AI Report",
                f,
                file_name="Heart_Report.pdf"
            )